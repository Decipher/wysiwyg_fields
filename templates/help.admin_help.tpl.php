<h2>Frequently asked questions</h2>
<dl>
  <dt><strong>Where did my CCK field go?</strong></dt>
  <dd>For usability purposes, Wysiwyg Fields consumes the fields that it is enabled on. If the field is required for use outside of the Wysiwyg it is recommended that you create a second field specifically for use with Wysiwyg Fields.</dd>

  <dt><strong>How can I see all the items on a Wysiwyg Field?</strong></dt>
  <dd>While the Wysiwyg Fields dialog is active you will see a <strong>+</strong> icon to the left of the dialog title, simply click the <strong>+</strong> to display the full CCK field view.</dd>
</dl>
