<h2>Configuration</h2>
<p>
  Wysiwyg Fields settings can be found with the standard CCK field settings:<br />
  &nbsp;&nbsp;Administer > Content management > Content types > [content type] > Manage fields > [field] > Configure<br />
  &nbsp;&nbsp;http://[www.yoursite.com/path/to/drupal]/admin/content/node-type/[content-type]/fields/[field]
</p>
<ul>
  <li>Expand the <strong>Wysiwyg Fields settings</strong> fieldset.</li>
  <li>Check the <strong>Attach to Wysiwyg?</strong> checkbox.</li>
  <li>Choose or upload icon for the Wysiwyg Field.</li>
  <li>Select at least one (1) formatter.</li>
  <li>(optional) Expand and configure the <strong>Advanced settings</strong>.</li>
</ul>

